package com.protostar.billingnstock.hr.entities;





public class TimeSheetTask {
	private String tid ;
	private String taskname ;
	private float monday ;
	private float tuesday ;
	private float wenesday ;
	private float thesday ;
	private float friday ;
	private float saturday ;
	private float sunday ;

	public float getMonday() {
		return monday;
	}
	public void setMonday(float monday) {
		this.monday = monday;
	}
	public float getTuesday() {
		return tuesday;
	}
	public void setTuesday(float tuesday) {
		this.tuesday = tuesday;
	}
	public float getWenesday() {
		return wenesday;
	}
	public void setWenesday(float wenesday) {
		this.wenesday = wenesday;
	}
	public float getThesday() {
		return thesday;
	}
	public void setThesday(float thesday) {
		this.thesday = thesday;
	}
	public float getFriday() {
		return friday;
	}
	public void setFriday(float friday) {
		this.friday = friday;
	}
	public float getSaturday() {
		return saturday;
	}
	public void setSaturday(float saturday) {
		this.saturday = saturday;
	}
	public float getSunday() {
		return sunday;
	}
	public void setSunday(float sunday) {
		this.sunday = sunday;
	}
	

	public String getTid() {
		return tid;
	}
	public void setTid(String tid) {
		this.tid = tid;
	}
	public String getTaskname() {
		return taskname;
	}
	public void setTaskname(String taskname) {
		this.taskname = taskname;
	}
	
	
	

	


	
}

